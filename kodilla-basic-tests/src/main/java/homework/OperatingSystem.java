package homework;

public class OperatingSystem{
        public int versionYear;

        public OperatingSystem(){
            this.versionYear = versionYear;
        }

        public void turnOn(){
            System.out.println("The operating system is turned on");
        }

        public void turnOff(){
            System.out.println("The operating system is turned off");
        }

        public int getVersionYear(){
            return getVersionYear();
        }

        public void displayVersionYear(){
            System.out.println("The operating system version year is" + " "+ versionYear);
        }
    }

